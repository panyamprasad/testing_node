// Difference between JavaScript & TypeScript?
    // JavaScript is the scripting language, we can run this in browser and nodeJs. TypeScript is superset of javaScript, we can declare the types statically.
    // JavaScript verify the types at run time. Typescript verify it compile time.
    // In Js error will appear at run time. In TS it will appear at compile time.
    // Js directly execute using Js engine. In TS we should compiled using javaScript.
    // File extension .js.. In TS file extension .ts, .tsx.
    // In JS all ES6 feature will support. In TS types, interfaces, classes, decorators...

// Notes:
    // Compare to javaScript, typeScript is more scalable and improve the performance.
    // Js using for scripting and small applications.
    // TS using for large applications and projects have multiple employees.

