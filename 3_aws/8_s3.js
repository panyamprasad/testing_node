        // It is used to store and retrive any amount of data (files, images, videos, backups, logs etc) from anywhere.

    // Why use S3?
        // Scalable: We can store unlimitted data.
        // Durable: 99.999% durabulity
        // Secure: Using IAM role permission so it will secure.
        // Cheap: Pay only what we used.
        // Flexible: Store static websites, logs, backups.
    
    // When use S3?
        // To store large files and it has the unlimitted store access.
        // Mainly used for disater recovery (safe and secure).
        // We can integrate lambda and cloud front for serverless apps.
    
    // What is max file size to upload s3?
        // For single file upload 5GB. But for multipart upload file size up to 5TB.
    
    // How does s3 provide durability?
